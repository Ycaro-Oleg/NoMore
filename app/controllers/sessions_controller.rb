class SessionsController < ApplicationController
  skip_before_action :authenticate_user!, only: [:new, :create]

  layout "auth"

  def new
    redirect_to root_path if current_user
  end

  def create
    user = User.find_by(email: params[:email])

    if user&.authenticate(params[:password])
      session[:user_id] = user.id
      redirect_to root_path, notice: "Back to face your commitments."
    else
      flash.now[:alert] = "Wrong credentials. Try again â€” or keep running from accountability."
      render :new, status: :unprocessable_entity
    end
  end

  def destroy
    session.delete(:user_id)
    redirect_to login_path, notice: "Logged out. Your commitments are still waiting."
  end
end
